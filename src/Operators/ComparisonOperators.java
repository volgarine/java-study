package Operators;

public class ComparisonOperators {
    public static void main(String[] args) {
        // > < <= >= == !+
        boolean isTenIsBiggerThanTwenty = 10 > 20;
        boolean isTenIsSmallerThanTwenty = 10 < 20;
        boolean isTenIsEqualThanTwenty = 10 == 20;
        boolean isTenIsDifferentThanTwenty = 10 != 20;
        System.out.println("10 is bigger than 20: " + isTenIsBiggerThanTwenty);
        System.out.println("10 is smaller than 20: " + isTenIsSmallerThanTwenty);
        System.out.println("10 is equal than 20: " + isTenIsEqualThanTwenty);
        System.out.println("10 is different than 20: " + isTenIsDifferentThanTwenty);

    }
}
